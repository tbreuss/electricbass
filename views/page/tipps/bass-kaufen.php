<h2 id="vier-fuenf-sechs-saiter">Vier, fünf oder sechs Saiten</h2>

<p>
    Der <b>E-Bass mit vier Saiten</b> gilt auch heute noch als Standard.
    Ein solches Instrument tönt gut, ist verhältnismässig leicht, sieht gut aus, ist bequem spielbar und deckt alle Anforderungen an ein Bassinstrument ab.
    Aus diesen Gründen spielen die meisten Bassisten - auch von bekannten Bands - einen 4-Saiter.
    Die Saiten sind bei einem E-Bass durchgehend in Quarten gestimmt und klingen eine Oktave tiefer als bei einer Gitarre.
    Die Standard-Stimmung eines viersaitigen E-Basses ist E-A-D-G und entspricht der Stimmung eines Kontrabasses.
</p>

<p>
    Bei einem <b>E-Bass mit 5 Saiten</b> kommt in der Regel eine tiefe Saite hinzu.
    Die Standard-Stimmung ist somit H-E-A-D-G.
    Eine tiefe H-Saite ist definitiv eine feine Sache.
    Damit kann man noch tiefere Töne spielen und ein satteres Fundament abliefern, und das ist ja die Hauptaufgabe eines Bassisten.
    Vor allem in modernen Musikspielen kann man die zusätzlichen Tieftöne gut gebrauchen und die Band im Gesamtsound unterstützen.
    Allerdings ist gerade die H-Saite bei den billigeren Ausführungen der Schwachpunkt am ganzen.
    Oft "schlabbert" sie undefiniert vor sich hin, hat keinen Druck oder geht im Bandkontext unter.
    Hinzu kommen die generellen Nachteile eines Fünsaiters: Er ist schwerer, weniger bequem zu spielen und in der Regel auch teurer.
</p>

<p>
    Bei einem <b>E-Bass mit 6 Saiten</b> kommt in der Regel noch einmal eine hohe Saite hinzu.
    Die Standard-Stimmung ist somit H-E-A-D-G-C.
    Auch das ist eine tolle Sache. Vorausgesetzt, man möchte sich als Bassist solistisch betätigen oder spielt in einer Band, wo dieser zusätzliche Tonumfang gefordert ist.
    Ansonsten ist der Ärger mit dem Gitarristen oder Pianisten vorprogrammiert, wenn man sich ständig in ihrem Tonbereich bewegt.
    Mit einem 6-Saiter schafft man sich aber auch Nachteile an: Der Bass ist deutlich schwerer und weniger bequem zu spielen und die Anschaffung noch einmal einen Zacken teurer.
</p>

